import mongoose from "mongoose";

const signUpSchema = new mongoose.Schema({
    UserId: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

const SignUp = mongoose.model("SignUp", signUpSchema);
export default SignUp;